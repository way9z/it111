public class MyVariable1 {

public static void main(String[] args) {
String name = "Wei"; // after a statement, don't forget the semi colon!
    int hours = 40; //we are assinging 40 to the hours variable
    //Please note, when we have a string vale, it is nested in ""
    // when we are referring to numbers/integers, no quotes!!!
    System.out.println(name);
    System.out.println(hours);
}
}
